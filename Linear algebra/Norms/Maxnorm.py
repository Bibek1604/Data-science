import numpy as np

max_norm= np.array([25, 2, 5])
max_value = np.max(np.abs(max_norm))
print(max_value)
# max norm
# answer is 25 because it is the maximum absolute value of the elements in the array
# In this case, the maximum absolute value of the elements in the array is 25.
